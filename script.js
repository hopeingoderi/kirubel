function applyTranslations(lang) {
  const dict = I18N[lang] || I18N.en;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) el.textContent = dict[key];
  });
  document.querySelectorAll("[data-i18n-ph]").forEach((el) => {
    const key = el.getAttribute("data-i18n-ph");
    if (dict[key]) el.setAttribute("placeholder", dict[key]);
  });
  const sel = document.getElementById("eventTypeSelect");
  if (sel) {
    const current = sel.value;
    const opts = lang === "de"
      ? ["Hochzeit", "Verlobung", "Taufe", "Feier/Event", "Anderes"]
      : ["Wedding", "Engagement", "Baptism", "Celebration/Event", "Other"];
    sel.innerHTML = "";
    opts.forEach((v) => {
      const o = document.createElement("option");
      o.value = v;
      o.textContent = v;
      sel.appendChild(o);
    });
    if (current) sel.value = current;
  }
}
function setLanguage(lang) {
  const safe = lang === "en" ? "en" : "de";
  localStorage.setItem("kv_lang", safe);
  document.documentElement.setAttribute("lang", safe);
  document.querySelectorAll(".langBtn").forEach((b) => {
    const active = b.getAttribute("data-lang") === safe;
    b.classList.toggle("is-active", active);
    b.setAttribute("aria-pressed", String(active));
  });
  applyTranslations(safe);
}

function setupNav() {
  const toggle = document.getElementById("navToggle");
  const nav = document.getElementById("mainNav");
  const backdrop = document.getElementById("navBackdrop");
  if (!toggle || !nav || !backdrop) return;
  const isMobile = () => window.matchMedia("(max-width: 980px)").matches;

  const openMenu = () => {
    nav.classList.add("is-open");
    backdrop.classList.add("is-open");
    toggle.setAttribute("aria-expanded", "true");
    backdrop.setAttribute("aria-hidden", "false");
    document.body.classList.add("nav-open");
  };
  const closeMenu = () => {
    nav.classList.remove("is-open");
    backdrop.classList.remove("is-open");
    toggle.setAttribute("aria-expanded", "false");
    backdrop.setAttribute("aria-hidden", "true");
    document.body.classList.remove("nav-open");
  };

  toggle.addEventListener("click", (e) => {
    e.preventDefault();
    e.stopPropagation();
    nav.classList.contains("is-open") ? closeMenu() : openMenu();
  });
  backdrop.addEventListener("click", closeMenu);
  nav.querySelectorAll('a[href^="#"]').forEach((a) => a.addEventListener("click", () => { if (isMobile()) closeMenu(); }));
  document.addEventListener("keydown", (e) => { if (e.key === "Escape") closeMenu(); });
  window.addEventListener("resize", () => { if (!isMobile()) closeMenu(); });
}

function setupAnchors() {
  document.querySelectorAll('a[href^="#"]').forEach((a) => {
    a.addEventListener("click", (e) => {
      const id = a.getAttribute("href");
      if (!id || id === "#") return;
      const el = document.querySelector(id);
      if (!el) return;
      e.preventDefault();
      el.scrollIntoView({ behavior: "smooth", block: "start" });
    });
  });
}
