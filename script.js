function setupShowreel() {
  const vid = document.getElementById("showreelVideo");
  const fallback = document.getElementById("showreelFallback");
  if (!vid || !fallback) return;
  const videos = ["assets/showreel/s1.mp4"];
  let i = 0;
  const slides = ["assets/showreel/s1.jpg","assets/showreel/s2.jpg","assets/showreel/s3.jpg","assets/showreel/s4.jpg"];
  function startFallback() {
    vid.classList.add("is-hidden");
    fallback.classList.remove("is-hidden");
    let j = 0;
    setInterval(() => {
      j = (j + 1) % slides.length;
      fallback.src = slides[j];
    }, 2800);
  }
  if (!videos.length) return startFallback();
  vid.src = videos[0];
  vid.addEventListener("error", startFallback);
  const p = vid.play();
  if (p && typeof p.catch === "function") p.catch(startFallback);
}
function setupTestimonials() {
  const track = document.getElementById("tTrack");
  const dotsWrap = document.getElementById("tDots");
  if (!track || !dotsWrap) return;
  const cards = Array.from(track.querySelectorAll(".tCard"));
  if (!cards.length) return;
  let idx = 0; let timer = null;
  dotsWrap.innerHTML = "";
  const dots = cards.map((_, i) => {
    const b = document.createElement("button");
    b.type = "button"; b.className = "tDot" + (i === 0 ? " is-active" : "");
    b.addEventListener("click", () => go(i, true));
    dotsWrap.appendChild(b); return b;
  });
  function apply() { track.style.transform = `translateX(${-idx * 100}%)`; dots.forEach((d, i) => d.classList.toggle("is-active", i === idx)); }
  function go(i, restart = false) { idx = (i + cards.length) % cards.length; apply(); if (restart) start(); }
  document.querySelector(".tPrev")?.addEventListener("click", () => go(idx - 1, true));
  document.querySelector(".tNext")?.addEventListener("click", () => go(idx + 1, true));
  function start() { if (timer) clearInterval(timer); timer = setInterval(() => go(idx + 1), 5200); }
  apply(); start();
}
function setupLightbox() {
  const items = Array.from(document.querySelectorAll(".gItem"));
  const lb = document.getElementById("lightbox");
  const img = document.getElementById("lbImg");
  const cap = document.getElementById("lbCap");
  const thumbsWrap = document.getElementById("lbThumbs");
  const btnClose = lb?.querySelector(".lbClose");
  const btnPrev = lb?.querySelector(".lbPrev");
  const btnNext = lb?.querySelector(".lbNext");
  const btnZoom = document.getElementById("lbZoom");
  const btnPlay = document.getElementById("lbPlay");
  if (!items.length || !lb || !img || !cap || !thumbsWrap) return;
  let index = 0, timer = null, scale = 1, tx = 0, ty = 0;
  const applyTransform = () => { img.style.transform = `translate(${tx}px, ${ty}px) scale(${scale})`; };
  const resetZoom = () => { scale = 1; tx = 0; ty = 0; lb.classList.remove("is-zoom"); img.style.cursor = "zoom-in"; applyTransform(); if (btnZoom) btnZoom.textContent = (localStorage.getItem("kv_lang")==="de"?"Zoom":"Zoom"); };
  const setLoading = (on) => { lb.classList.toggle("is-loading", on); lb.classList.toggle("is-ready", !on); };
  function updatePlayLabel(){ if(btnPlay) btnPlay.textContent = timer ? (localStorage.getItem("kv_lang")==="de"?"Pause":"Pause") : "Play"; }
  function startSlideshow(){ stopSlideshow(); timer=setInterval(()=>next(),3600); updatePlayLabel(); }
  function stopSlideshow(){ if(timer) clearInterval(timer); timer=null; updatePlayLabel(); }
  let thumbButtons=[];
  function buildThumbs(){
    thumbsWrap.innerHTML="";
    thumbButtons = items.map((a,i)=>{
      const btn=document.createElement("button"); btn.type="button"; btn.className="lbThumb";
      const t=document.createElement("img"); t.src=a.getAttribute("href"); t.alt=a.getAttribute("data-caption")||`Thumb ${i+1}`;
      btn.appendChild(t); btn.addEventListener("click",()=>open(i)); thumbsWrap.appendChild(btn); return btn;
    });
  }
  function setActiveThumb(){ thumbButtons.forEach((b,i)=>b.classList.toggle("is-active", i===index)); }
  function open(i){
    index=i; const el=items[index]; const href=el.getAttribute("href"); const caption=el.getAttribute("data-caption")||"";
    cap.textContent=caption; lb.classList.add("is-open"); lb.setAttribute("aria-hidden","false"); document.body.style.overflow="hidden";
    setActiveThumb(); resetZoom(); setLoading(true);
    const pre = new Image(); pre.onload=()=>{ img.src=href; img.alt=caption||"Preview"; requestAnimationFrame(()=>{ setLoading(false); startSlideshow(); }); }; pre.src=href;
  }
  function close(){ lb.classList.remove("is-open","is-loading","is-ready","is-zoom"); lb.setAttribute("aria-hidden","true"); document.body.style.overflow=""; img.src=""; cap.textContent=""; stopSlideshow(); resetZoom(); }
  function prev(){ open((index-1+items.length)%items.length); }
  function next(){ open((index+1)%items.length); }
  buildThumbs();
  items.forEach((a,i)=>a.addEventListener("click",(e)=>{ e.preventDefault(); open(i); }));
  btnClose?.addEventListener("click",close); btnPrev?.addEventListener("click",()=>{ resetZoom(); prev(); }); btnNext?.addEventListener("click",()=>{ resetZoom(); next(); });
  btnPlay?.addEventListener("click", ()=> timer ? stopSlideshow() : startSlideshow());
  btnZoom?.addEventListener("click", ()=>{ stopSlideshow(); if(scale===1){ scale=2; lb.classList.add("is-zoom"); img.style.cursor="grab"; applyTransform(); btnZoom.textContent = localStorage.getItem("kv_lang")==="de"?"Reset":"Reset"; } else resetZoom(); });
  document.addEventListener("keydown",(e)=>{ if(!lb.classList.contains("is-open")) return; if(e.key==="Escape") close(); if(e.key==="ArrowLeft") prev(); if(e.key==="ArrowRight") next(); });
  img.addEventListener("click",(e)=>{ e.stopPropagation(); stopSlideshow(); if(scale===1){ scale=2; lb.classList.add("is-zoom"); applyTransform(); } else resetZoom(); });
}

